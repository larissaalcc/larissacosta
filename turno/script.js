let turno = (prompt("Em que turno você estuda?"));

if (turno == 'V' || turno== 'v'){
    window.alert("Boa Tarde!");
}
else if (turno == 'M'|| turno == 'm'){
    window.alert("Bom Dia!");
}
else if (turno == 'N'||turno == 'n'){
    window.alert("Boa Noite!");
}
else{
    window.alert('Turno inválido');
}
    